    private Object[] pasarALista(int CantDeValores, double[] numeros) {
        Object[] lista = new Object[CantDeValores];
        for (int i = 0; i < numeros.length; i++) {
            double d = numeros[i];
            lista[i] = d;
        }
        return lista;
    }

    private void jbSimularPoissonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSimularPoissonActionPerformed

        int cantDeValores = Integer.parseInt(jtNumAGenerarPoisson.getText());
        double media = Double.parseDouble(tfMediaPoisson.getText());
        double[] numerosAleatorios = g.poisson(cantDeValores, media);

        Object[] listaObject = pasarALista(cantDeValores, numerosAleatorios);
        jlNumerosAleatoriosGenerados.setListData(listaObject);

        graficarHistograma(calcularHistograma(numerosAleatorios,Integer.parseInt(jcbIntervalosPoisson.getSelectedItem().toString()), true));//jcbIntervalosPoisson  --> jcbIntervalosCongruencia

    }//GEN-LAST:event_jbSimularPoissonActionPerformed

    private void jbSimularExponencialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSimularExponencialActionPerformed

        int cantDeValores = Integer.parseInt(jtNumAGenerarExponencial.getText());
        double media = Double.parseDouble(tfMediaExponencial.getText());
        double[] numerosAleatorios = g.exponencial(cantDeValores, media);

        Object[] listaObject = pasarALista(cantDeValores, numerosAleatorios);
        jlNumerosAleatoriosGenerados.setListData(listaObject);

        graficarHistograma(calcularHistograma(numerosAleatorios,
                Integer.parseInt(jcbIntervalosExponencial.getSelectedItem().toString()),
                false));
    }//GEN-LAST:event_jbSimularExponencialActionPerformed

